class ProductModel {
  final int id;
  final String name;
  final String subName;
  final String foodImage;
  final String restImage;
  final String time;

  final int currentPrice;


  ProductModel({
    required this.id,
    required this.name,
    required this.subName,
    required this.foodImage,
    required this.restImage,
    required this.time,
    required this.currentPrice,
  });


}


